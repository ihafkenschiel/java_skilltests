    static long arrayManipulation(int n, int[][] queries) {
        long[] slope = new long[n];
        
        for (int i=0; i<queries.length; i++) {
            int start = queries[i][0];
            int end = queries[i][1];
            int add = queries[i][2];
            slope[start-1] += add;
            if (end<slope.length) {
                slope[end] -= add;
            }
        }
        
        long max = 0;
        long sum = 0;
        for (int i=0; i<slope.length; i++) {
            sum += slope[i];
            if (sum > max) {
                max = sum;
            }
        }
        //System.out.println(Arrays.toString(slope));

        return max;
    }

